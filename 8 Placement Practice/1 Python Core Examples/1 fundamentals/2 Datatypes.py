# Integer

num = 100

print(num)
print(type(num))


# Float

price = 199.99

print(price)
print(type(price))


# String

language = "Python"

print(language)
print(type(language))


# Boolean

is_active = True

print(is_active)
print(type(is_active))


# Complex

complex_num = 2 + 3j

print(complex_num)
print(type(complex_num))


# Type conversion

age = "25"

print(type(age))

age = int(age)

print(age)
print(type(age))


# Checking data types

value = 99.5

print(isinstance(value, float))
print(isinstance(value, int))


# None type

result = None

print(result)
print(type(result))